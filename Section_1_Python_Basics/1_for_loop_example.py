print('My name is')
for i in range(5):
    print('Jimmy Five Times ' + str(i))

# Output is:
# My name is
# Jimmy Five Times 0
# Jimmy Five Times 1
# Jimmy Five Times 2
# Jimmy Five Times 3
# Jimmy Five Times 4

# The vairable "i" is set to 0 to begin. So running five times in the range ends
# with 4.
# Once the first execution is run it resets and runs the loop again as many times
# as you indicated.
# The range always starts at ZERO so it never includes the number you type in
# the range.

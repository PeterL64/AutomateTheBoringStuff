def hello(name):
    print ('Hello ' + name)

hello('Alice')              # We will call the function and add an arguement.
hello('Bob')


# The strings 'Alice' and 'Bob' are called ARGUEMENTS.
# The variable "name" is called a PARAMETER

# When the function is called, the name parameter is assigned the arguement that
# is passed. i.e. the name variable is assigned a value, Alice or Bob.


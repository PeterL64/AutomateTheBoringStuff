import pyperclip              # Pyperclip module has a copy and a paste function
pyperclip.copy('Hello World') # Copy function copies to the clipboard
pyperclip.paste()             # This will paste whatever text is on the clipboard

# In the interactive shell you can try this code. You can also see that the word
# copied is in the clipboard by right clicking and selecting paste.


spam = 0
while spam < 5:
    print('Hello World')
    spam = spam + 1

# This is a "while loop". spam starts off as 0 and the program runs and prints
# "Hello World". Once it has ran once, it loops back to the start and runs
# aagain. Now "spam" equals "spam + 1", ie "1".
# So, this loop prints the output from the print function until spam is no longer
# less than 5. The Execution stops.
# When the execution runs through a loop, we call that an iteration. So we say
# this while loop iterates 5 times. On each iteration the smap variable was
# increased by 1.
